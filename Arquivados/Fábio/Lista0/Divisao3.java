public class Divisao3{
    public static void main(String[] args){
      
        for (int i = 0; i <= 100; i++) {
            
            if(i % 3 == 0){
                System.out.println();
                System.out.println(i+" é divisível por 3");
                System.out.println(i+" Dividido por 2: "+i/2);
            }
        }    
    }
}